export const suma = (a: number, b: number): number => a + b;
export const resta = (a: number, b: number): number => a - b;
export const multiplicacion = (a: number, b: number): number => a * b;
export const division = (a: number, b: number): number => a / b;
export const potencia = (a: number, b: number): number => a ** b;
export const raiz = (a: number): number => Math.sqrt(a);
export const multiplicacionPorPI = (a: number): number => a * Math.PI;
